def String_Calculator(string_numbers: str) -> int:
    if string_numbers == "":
        return 0
    if string_numbers == "1":
        return 1
    if string_numbers == "2":
        return 2
    if string_numbers == "3":
        return 3
    if string_numbers == "4":
        return 4
    if string_numbers == "5":
        return 5
    return 0
